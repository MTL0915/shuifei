import water08b from "@/components/daping/water/water08b.vue";
import water09a from "@/components/daping/water/water09a.vue";
import water09b from "@/components/daping/water/water09b.vue";
import water10a from "@/components/daping/water/water10a.vue";
import water10b from "@/components/daping/water/water10b.vue";
import water11a from "@/components/daping/water/water11a.vue";
import water11b from "@/components/daping/water/water11b.vue";
import water12 from "@/components/daping/water/water12.vue";
export default {
  mounted() {
    // 一开始先触发
    this.tiaozhengshuiliu();
    // 事件总线，开关变化就会触发这个事件
    this.$bus.$on("waterEvent", () => {
      // 这里一定要用箭头函数
      this.tiaozhengshuiliu();
    });
  },
  components: {
    water01,
    water02,
    water03,
    water04,
    water05a,
    water05b,
    water06a,
    water06b,
    water07a,
    water07b,
    water08a,
    water08b,
    water09a,
    water09b,
    water10a,
    water10b,
    water11a,
    water11b,
    water12,
  },
  methods: {
    tiaozhengshuiliu() {
      // 获取所有流水
      var shuiliuBox = document.getElementsByClassName("shuiliu");
      shuiliuBox = Array.prototype.slice.apply(shuiliuBox);
      // console.log(shuiliuBox)
      // 获取肥水混合剂,加到流水里一起
      var hunheji = document.getElementsByClassName("hunheji");
      hunheji = Array.prototype.slice.apply(hunheji);
      var shuiliuBox = shuiliuBox.concat(hunheji);

      // 调整水流的逻辑
      for (var i = 0; i < shuiliuBox.length; i++) {
        // 获取开着的按钮
        var openKey = this.$store.state.btn.openKey;
        var shuiliuItem = shuiliuBox[i];
        var where = shuiliuItem.getAttribute("where");
        if (!where) continue;
        where = where.split(",").map(Number);
        var or = shuiliuItem.getAttribute("or");
        if (or) or = or.split(",").map(Number);
        var bool = true;
        for (var ii in where) {
          if (openKey.indexOf(where[ii]) === -1) {
            bool = false;
            break;
          }
        }
        if (or && bool) {
          bool = false;
          for (var ii in or) {
            if (openKey.indexOf(or[ii]) !== -1) {
              bool = true;
              break;
            }
          }
        }
        var bool1 = true;
        var where1 = shuiliuItem.getAttribute("where1");
        if (!where1) {
          bool1 = false;
        } else {
          where1 = where1.split(",").map(Number);
          var or1 = shuiliuItem.getAttribute("or1");
          if (or1) or1 = or1.split(",").map(Number);
          for (var ii in where1) {
            if (openKey.indexOf(where1[ii]) === -1) {
              bool1 = false;
              break;
            }
          }
          if (or1 && bool1) {
            bool1 = false;
            for (var ii in or1) {
              if (openKey.indexOf(or1[ii]) !== -1) {
                bool1 = true;
                break;
              }
            }
          }
        }
        if (bool || bool1) {
          shuiliuItem.style.display = "block";
        } else {
          shuiliuItem.style.display = "none";
        }
      }
      // 事件总线触发灌溉阀事件
      this.$bus.$emit("guangaifaEvent");
    },
  },
};
</script>

