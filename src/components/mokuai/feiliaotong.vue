      // 小按钮开关的切换
      if (this.pots[index].zhushuifa.value == 0) {
        this.pots[index].zhushuifa.value = 1;
      } else {
        this.pots[index].zhushuifa.value = 0;
      }
      this.tiaozhengSmallWater(code);
    },

